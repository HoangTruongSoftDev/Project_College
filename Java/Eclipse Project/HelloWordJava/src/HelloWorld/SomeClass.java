package HelloWorld;

public class SomeClass {
	byte aByte;
	int anInt;
	float aFloat;
	boolean aBool;
	public void displayAttributeValues() {
		System.out.println("aByte = " + aByte);
		System.out.println("aaInt = " + anInt);
		System.out.println("aFloat = " + aFloat);
		System.out.println("aBool = " + aBool);
	}
}
